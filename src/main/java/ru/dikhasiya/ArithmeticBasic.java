package ru.dikhasiya;

public class ArithmeticBasic {
    public static void main(String[] args) {
        int a = 11;
        int b = 22;
        int c = 33;
        int d = 44;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("b / a = " + (b / a));
        System.out.println("b % a = " + (b % a));
        System.out.println("c % a = " + (c % a));
    }
}
