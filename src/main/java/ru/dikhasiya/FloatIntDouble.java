package ru.dikhasiya;

public class FloatIntDouble {
    public static void main(String[] args) {

        int numberInt = 10;
        double numberdouble = 111.111;
        int c = 222;
        double d = 63.1;

        System.out.println("numberInt + numberdouble =" + (numberInt + numberdouble));
        System.out.println("c * d =" + (c * d));
    }
}
